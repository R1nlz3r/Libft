#include "libft.h"

char	*ft_strdup(const char *s)
{
	char		*dup;
	size_t		i;

	i = 0;
	if (!(dup = (char*)malloc(ft_strlen(s) + 1)))
		return (NULL);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
